a=int(input("Adj meg egy szamot: "))
b=int(input("Adj meg  meg egy szamot: "))
c=input("Adj meg egy muveleti jelet: ")


if c=="+":
    print("Az eredmeny =",+ a+b)
elif c=="-":
    print("Az eredmeny =",+a-b)
elif c=="*":
    print("Az eredmeny =",+a*b)
elif c=="/":
    print("Az eredmeny =",+a/b)
elif c=="**":
    print("Az eredmeny =",+a**b)
elif c=="%":
    print("Az eredmeny =",+a%b)
elif c=="//":
    print("Az eredmeny =",+a//b)


